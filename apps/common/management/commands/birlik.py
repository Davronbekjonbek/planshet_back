from apps.form.models import Birlik


class Command(BaseCommand):
    help = "birliklarni bazaga yuklaydi"

    def handle(self, *args, **options):

        code = 1
        for i in units:
            if not Birlik.objects.filter(name=i).exists():
                Birlik.objects.create(name=i, code=code, miqdor=1)
                code += 1

        self.stdout.write(self.style.SUCCESS("Import jarayoni muvaffaqiyatli yakunlandi!"))


